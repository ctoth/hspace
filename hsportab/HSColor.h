#pragma once

class HSColor
{
public:
  HSColor()
    : _r(0), _g(0), _b(0), _a(255)
  {
  }

  HSColor(unsigned char aRed, unsigned char aGreen, unsigned char aBlue)
    : _r(aRed), _g(aGreen), _b(aBlue), _a(255)
  {
  }

  unsigned long ARGB() const
  {
    return (_a << 24) | (_r << 16) | (_g << 8) | _b;
  }

  unsigned char _r, _g, _b, _a;
};
